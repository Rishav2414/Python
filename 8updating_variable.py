#increment and decrement variable
#---------------------------------

a = 6 # initilize x
print(a) # print: 6

a += 3 # increment a by 3; same as a = a + 1 -> a=6+3 = 9
print(a) # print: 7

a -= 1 #decrement a by 1; same as a = a - 1 -> a=9-1 = 8
print(a) #print: 8
